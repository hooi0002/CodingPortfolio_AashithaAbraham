# Decision making with conditionals

"""
Variations of conditionals
1. if
2. if - elif(s)
3. if - elif(s) - else
4. if - else
"""

score = 10

# Executes the first condition that is evaluated to True and disregard the rest
if score > 1:
    print("#_#")

elif score > 70:
    print("*_*")

elif score > 50:
    print("&_&")

else:
    print("X_X")


#####################################

if score > 5:
    print("^_^")

if score > 7:
    print("@_@")

if score > 9:
    print("T_T")














